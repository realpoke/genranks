<?php

namespace App\Filament\Resources;

use App\Filament\Resources\GameUserResource\Pages;
use App\Filament\Resources\GameUserResource\RelationManagers;
use App\Models\GameUser;
use Filament\Forms;
use Filament\Forms\Form;
use Filament\Resources\Resource;
use Filament\Tables;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\SoftDeletingScope;

class GameUserResource extends Resource
{
    protected static ?string $model = GameUser::class;

    protected static ?string $navigationIcon = 'heroicon-o-rectangle-stack';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Forms\Components\Select::make('user_id')
                    ->relationship('user', 'name')
                    ->required(),
                Forms\Components\Select::make('game_id')
                    ->relationship('game', 'id')
                    ->required(),
                Forms\Components\TextInput::make('elo_change')
                    ->numeric()
                    ->default(null),
                Forms\Components\Textarea::make('header')
                    ->required()
                    ->columnSpanFull(),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                Tables\Columns\TextColumn::make('user.name')
                    ->numeric()
                    ->sortable(),
                Tables\Columns\TextColumn::make('game.id')
                    ->numeric()
                    ->sortable(),
                Tables\Columns\TextColumn::make('elo_change')
                    ->numeric()
                    ->sortable(),
                Tables\Columns\TextColumn::make('created_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
                Tables\Columns\TextColumn::make('updated_at')
                    ->dateTime()
                    ->sortable()
                    ->toggleable(isToggledHiddenByDefault: true),
            ])
            ->filters([
                //
            ])
            ->actions([
                Tables\Actions\EditAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\BulkActionGroup::make([
                    Tables\Actions\DeleteBulkAction::make(),
                ]),
            ]);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListGameUsers::route('/'),
            'create' => Pages\CreateGameUser::route('/create'),
            'edit' => Pages\EditGameUser::route('/{record}/edit'),
        ];
    }
}
